    public void definirTablar(int opcionBuscar, String valor){
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.addColumn("RUT");
        tabla.addColumn("NOMBRE");
        tabla.addColumn("GENERO");
        tabla.addColumn("EDAD");
        tabla.addColumn("DIRECCION");
        tabla.addColumn("CIUDAD");
        tabla.addColumn("ISAPRE");
        tabla.addColumn("DONANTE");
        tablaCliente.setModel(tabla);
        
        String codSql;  
        
        if (opcionBuscar==0 && valor==null) {
            codSql = "SELECT * FROM paciente";
        }else {
           if(opcionBuscar==1 && valor != null){
             codSql = "SELECT * FROM paciente WHERE rut = '"+valor+"'";
           }else{
               if (opcionBuscar==2 && valor!=null) {
                   codSql  = "SELECT * FROM paciente WHERE nombre = '"+valor+"'";
               }else{
                   if (opcionBuscar==3 && valor != null) {
                       codSql = "SELECT * FROM paciente WHERE direccion ='"+valor+"'";
                   }else{
                       codSql = "SELECT * FROM paciente";
                   }
               }
           }
        
        
        }
        
        String []datos = new String[8];
        try {
            Statement st = connect.createStatement();
            ResultSet rs = st.executeQuery(codSql); 
            
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                tabla.addRow(datos);
                
            }
            tablaCliente.setModel(tabla);
        } catch (Exception e) {
            System.out.println(e);
        }
    
    
    
    
    
    
    
    
    }
    
    
    
    
    
